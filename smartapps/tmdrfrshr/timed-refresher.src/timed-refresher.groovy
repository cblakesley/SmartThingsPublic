definition(
    name: "Timed Refresher",
    namespace: "TmdRfrshr",
    author: "Christopher Blakesley",
    description: "Refresh selected devices on a repeating timer",
    category: "My Apps",
    iconUrl: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience.png",
    iconX2Url: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience@2x.png",
    iconX3Url: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience@2x.png")


preferences {
    section("Refresh this device") {
        input "devRefresh", "capability.polling", required: true
    }
}

def installed() {
	log.debug "Installed with settings: ${settings}"

	initialize()
}

def updated() {
	log.debug "Updated with settings: ${settings}"

	unsubscribe()
	initialize()
}

def initialize() {

    runEvery5Minutes(hndlRefresh)
}




def hndlRefresh(evt) {

	log.debug("hndlRefresh called: $evt")
    
    devRefresh.poll()
}

